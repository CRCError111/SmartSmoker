-- Проверяем текущее состояние
SELECT 
    id,
    device_id,
    name,
    user_id,
    status,
    unbound,
    created_at
FROM devices 
WHERE device_id = '5f281f44-7a6a-4bd5-874c-af5522494f71';

-- Обновляем user_id для устройства
-- user_id = 3 это crcerror11@gmail.com
UPDATE devices 
SET user_id = 3
WHERE device_id = '5f281f44-7a6a-4bd5-874c-af5522494f71';

-- Проверяем результат
SELECT 
    id,
    device_id,
    name,
    user_id,
    status,
    unbound,
    created_at
FROM devices 
WHERE device_id = '5f281f44-7a6a-4bd5-874c-af5522494f71';

-- =====================================================
-- Проверка: устройство должно появиться на странице
-- =====================================================
-- Запрос, который использует devices.php:
SELECT * FROM devices 
WHERE user_id = 3 
AND (unbound IS NULL OR unbound = 0) 
ORDER BY created_at DESC;

