-- =====================================================
-- Исправление имени файла прошивки v2.1.0
-- =====================================================
-- Проблема: В БД указано SmartSmoker_v2.1.0.bin
--           На сервере файл называется firmware_2.1.0.bin
-- =====================================================

-- Обновляем имя файла в базе данных
UPDATE firmware_updates 
SET filename = 'firmware_2.1.0.bin'
WHERE version = '2.1.0';

-- Проверка
SELECT id, version, filename, file_size, is_active, checksum
FROM firmware_updates 
WHERE version = '2.1.0';

-- =====================================================
-- После выполнения этого скрипта:
-- - База данных будет ссылаться на firmware_2.1.0.bin
-- - Файл на сервере: firmware_2.1.0.bin
-- - ESP32 сможет скачать прошивку
-- =====================================================
